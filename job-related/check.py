def check(board):
    
    kx = 0
    ky = 0
    qx = 0
    qy = 0
    
    for i in range(len(board)):
        for j in range(len(board[i])):
            if board[i][j] == 'k':
                kx = i
                ky = j
            if board[i][j] == 'q':
                qx = i
                qy = j

    if kx == qx:
        return True
    elif ky == qy:
        return True
    elif abs(kx - qx) == abs(ky-qy):
        return True
    else:
        return False

